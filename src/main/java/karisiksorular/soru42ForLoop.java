package karisiksorular;

public class soru42ForLoop {
    public static void main(String[] args) {
    //100 den başlayarak 50'ye(50dahil) kadar olan sayıları aralarında virgül
    // olarak ayni satırda  yazdırın

        for(int i=100; i>=50; i--){
            System.out.print(","+i);

        }


    }
}
